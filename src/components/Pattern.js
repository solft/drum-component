const Pattern = ({ pattern }) => {
  const displayPattern = Object.entries(pattern).map(drumLine => {
    const drumDisplay = drumLine[1].map((time, index) => (<span key={index}> {time} </span>))
    return (
      <div key={drumLine[0]}>
        <span>{drumLine[0]} </span>
        {drumDisplay}
      </div>
    )
  })
  return (
    <div>
      {displayPattern}
    </div>
  )
}

export default Pattern
